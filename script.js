const trigger = document.getElementById('random-trigger');
const displayImage = document.getElementById('display-image');
const displayVerse = document.getElementById('display-verse');

// 랜덤 선택 함수 (배열에서 하나를 무작위로 뽑음)
function getRandomItem(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// 콘텐츠 변경 함수
function changeContent() {
    // 페이드 효과 시작 (투명하게)
    trigger.style.opacity = 0;
    
    setTimeout(() => {
        // 이미지와 시구를 각각 따로 뽑아서 적용
        const randomImage = getRandomItem(images);
        const randomVerse = getRandomItem(verses);

        displayImage.src = randomImage;
        displayVerse.innerHTML = randomVerse;

        // 페이드 효과 끝 (다시 보이게)
        trigger.style.opacity = 1;
    }, 300);
}

// 클릭 이벤트 리스너
trigger.addEventListener('click', changeContent);
changeContent();